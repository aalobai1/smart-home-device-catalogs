#!/usr/bin/env node
/**
 * Generate an agent-friendly catalog of Home Assistant integrations.
 *
 * Home Assistant integrations are the "building blocks" that let HA talk to a
 * device, service, or protocol. This catalog lists every integration shipped in
 * Home Assistant Core so an agent building on top of HA knows what it can
 * already connect to (and how — locally vs cloud, push vs polling).
 *
 * Source of truth: the official integrations index published by the Home
 * Assistant project, generated from each integration's manifest + docs.
 *   https://www.home-assistant.io/integrations.json   (no auth required)
 *
 * Outputs:
 *   home-assistant-integrations.md     Human/agent-readable Markdown
 *   home-assistant-integrations.json   Same data, machine-readable
 *
 * Run:  node generate-homeassistant.mjs   (Node 18+, no dependencies)
 */

import { writeFile } from "node:fs/promises";

const SRC = "https://www.home-assistant.io/integrations.json";
const UA = { "User-Agent": "home-assistant-catalog-md/1.0" };
const DOC = (domain) => `https://www.home-assistant.io/integrations/${domain}/`;

// integration_type -> short explanation (Home Assistant taxonomy)
const TYPE_DESC = {
  device: "Talks to a single device",
  hub: "Connects a hub/bridge that exposes multiple devices",
  service: "Connects an online service or API",
  integration: "General integration",
  virtual: "Points to another integration that provides the support",
  brand: "A brand umbrella covering several integrations",
  helper: "Built-in helper (no external device/service)",
  entity: "Provides entity platforms used by other integrations",
  system: "Core system integration",
  hardware: "Hardware/board support",
};

const esc = (s) => String(s ?? "").replaceAll("|", "\\|").replaceAll("\n", " ");

async function main() {
  const res = await fetch(SRC, { headers: UA });
  if (!res.ok) throw new Error(`${res.status} ${SRC}`);
  const raw = await res.json();

  const items = Object.entries(raw).map(([domain, meta]) => ({
    domain,
    name: (meta.title || domain).trim(),
    type: meta.integration_type || "integration",
    iotClass: meta.iot_class || "",
    qualityScale: meta.quality_scale || "",
    description: (meta.description || "").trim(),
    docs: DOC(domain),
  }));
  items.sort((a, b) => a.name.toLowerCase().localeCompare(b.name.toLowerCase()));

  const today = new Date().toISOString().slice(0, 10);
  await writeFile(
    "home-assistant-integrations.json",
    JSON.stringify({ source: SRC, generated: today, count: items.length, integrations: items }, null, 2)
  );

  const byType = new Map();
  const byIot = new Map();
  for (const it of items) {
    byType.set(it.type, (byType.get(it.type) ?? 0) + 1);
    const cls = it.iotClass || "Unspecified";
    byIot.set(cls, (byIot.get(cls) ?? 0) + 1);
  }
  const n = (x) => x.toLocaleString("en-US");

  const L = [];
  L.push("# Home Assistant Integrations", "");
  L.push("> Agent-friendly catalog of every integration shipped with **Home Assistant**, the");
  L.push("> open-source home automation platform. Each integration is a building block that lets");
  L.push("> Home Assistant connect to a device, hub, service, or protocol. Use this to know what");
  L.push("> Home Assistant can already talk to when building on top of it.", "");
  L.push(
    `**Source:** [Home Assistant integrations index](${SRC}) — generated by the Home Assistant ` +
      "project from each integration's manifest and documentation.",
    ""
  );
  L.push(`**Generated:** ${today}  `);
  L.push(`**Integrations:** ${n(items.length)}`, "");
  L.push(
    "Columns: **Type** is how the integration connects (see table below). **IoT class** tells you " +
      "whether communication is *local* or *cloud* and *push* or *polling* — important for latency, " +
      "privacy, and offline behavior. `domain` is HA's internal id and the docs slug " +
      "(`https://www.home-assistant.io/integrations/<domain>/`). Regenerate with `npm run homeassistant`.",
    ""
  );

  L.push("## Integration types", "");
  L.push("| Type | Meaning | Count |", "|---|---|---|");
  for (const [t, c] of [...byType.entries()].sort((a, b) => b[1] - a[1])) {
    L.push(`| \`${t}\` | ${TYPE_DESC[t] ?? "—"} | ${c} |`);
  }
  L.push("");

  L.push("## Connectivity (IoT class)", "");
  L.push("| IoT class | Count |", "|---|---|");
  for (const [c, count] of [...byIot.entries()].sort((a, b) => b[1] - a[1])) {
    L.push(`| ${c} | ${count} |`);
  }
  L.push("");

  L.push("## All integrations", "");
  L.push(`_${n(items.length)} integrations, alphabetical._`, "");
  L.push("| Integration | Domain | Type | IoT class | Description |", "|---|---|---|---|---|");
  for (const it of items) {
    L.push(
      `| [${esc(it.name)}](${it.docs}) | \`${it.domain}\` | ${it.type} | ${it.iotClass || "—"} | ${esc(it.description || "—")} |`
    );
  }
  L.push("");

  await writeFile("home-assistant-integrations.md", L.join("\n"));
  console.log(`Wrote home-assistant-integrations.md and .json (${items.length} integrations)`);
}

main().catch((e) => {
  console.error(e);
  process.exit(1);
});
